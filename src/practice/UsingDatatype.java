package practice;

public class UsingDatatype {
    public static void main(String[] args) {
        int value1=10;
        int value2=20;
        int add=value1+value2;
        System.out.println(add);


        int sub=10*20;
        System.out.println(sub);

        float num=33.5f-3.5f;
        System.out.println(num);

        byte a=10;
        short b=20;
        int c=30;
        int number=b+c;
        float num1=44.5f+ a;
        System.out.println(num1);
    }
}
