package practice;

public class incrementDecrement {
    public static void main(String[] args) {


        int i = 20;
        System.out.println(++i);
        System.out.println(i);
        System.out.println(i++);
        System.out.println(i);

        int y=7;
        int x=++y*8;
        System.out.println(x);

        char ch ='A';
        System.out.println(ch++);
        System.out.println(++ch);
    }
}
