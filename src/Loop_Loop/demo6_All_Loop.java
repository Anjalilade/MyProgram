package Loop_Loop;

public class demo6_All_Loop {
    public static void main(String[] args) {
        System.out.println("For Loop");
        for (int i = 0; i <5 ; i++) {
            System.out.println("Hello World");

        }
        System.out.println("While Loop");
        int i=0;
        while (i<5){
            System.out.println("Hello World");
            i++;
        }
        System.out.println("Do While Loop");
        int counter=0;
        do {
            System.out.println("Hello World");
            counter++;
        }while (counter<5);
    }
}
